void main() {
  // For Loop
  print("For Loop:");

  for (int i = 1; i <= 5; i++) {
    print(i);
  }

  // While Loop
  print("\nWhile Loop:");

  int number = 1;

  while (number <= 5) {
    print(number);
    number++;
  }

  // Do-While Loop
  print("\nDo-While Loop:");

  int count = 1;

  do {
    print(count);
    count++;
  } while (count <= 5);
}